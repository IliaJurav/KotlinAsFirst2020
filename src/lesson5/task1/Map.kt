/**
 * Сложная (5 баллов)
 *
 * Для заданного ассоциативного массива знакомых через одно рукопожатие `friends`
 * необходимо построить его максимальное расширение по рукопожатиям, то есть,
 * для каждого человека найти всех людей, с которыми он знаком через любое
 * количество рукопожатий.
 *
 * Считать, что все имена людей являются уникальными, а также что рукопожатия
 * являются направленными, то есть, если Марат знает Свету, то это не означает,
 * что Света знает Марата.
 *
 * Оставлять пустой список знакомых для людей, которые их не имеют (см. EvilGnome ниже),
 * в том числе для случая, когда данного человека нет в ключах, но он есть в значениях
 * (см. GoodGnome ниже).
 *
 * Например:
 *   propagateHandshakes(
 *     mapOf(
 *       "Marat" to setOf("Mikhail", "Sveta"),
 *       "Sveta" to setOf("Marat"),
 *       "Mikhail" to setOf("Sveta"),
 *       "Friend" to setOf("GoodGnome"),
 *       "EvilGnome" to setOf()
 *     )
 *   ) -> mapOf(
 *          "Marat" to setOf("Mikhail", "Sveta"),
 *          "Sveta" to setOf("Marat", "Mikhail"),
 *          "Mikhail" to setOf("Sveta", "Marat"),
 *          "Friend" to setOf("GoodGnome"),
 *          "EvilGnome" to setOf(),
 *          "GoodGnome" to setOf()
 *        )
 */
fun propagateHandshakes(friends: Map<String, Set<String>>): Map<String, Set<String>> {
    lateinit var sf: (Set<String>, MutableSet<String>) -> Set<String>
    sf = { ss, ex ->
        val ee = mutableSetOf<String>()
        for (n in ss)
            if (n !in ex) {
                ex.add(n)
                if (n in friends) ee.addAll(sf(friends.getValue(n), ex))
                ee.add(n)
            }
        ee
    }
    return (friends.keys + friends.values.fold(setOf()) { acc, e -> acc + e })
        .toList()
        .map {
            it to if (it in friends) sf(friends.getValue(it), mutableSetOf(it)).sorted()
                .toSet() else setOf()
        }
        .toMap()
}

/**
 * Сложная (6 баллов)
 *
 * Для заданного списка неотрицательных чисел и числа определить,
 * есть ли в списке пара чисел таких, что их сумма равна заданному числу.
 * Если да, верните их индексы в виде Pair<Int, Int>;
 * если нет, верните пару Pair(-1, -1).
 *
 * Индексы в результате должны следовать в порядке (меньший, больший).
 *
 * Постарайтесь сделать ваше решение как можно более эффективным,
 * используя то, что вы узнали в данном уроке.
 *
 * Например:
 *   findSumOfTwo(listOf(1, 2, 3), 4) -> Pair(0, 2)
 *   findSumOfTwo(listOf(1, 2, 3), 6) -> Pair(-1, -1)
 */
fun findSumOfTwo(list: List<Int>, number: Int): Pair<Int, Int> {
    val setForSearch = list.toSet()
    for (a in setForSearch) {
        if (setForSearch.contains(number - a)) {
            val min = list.indexOf(a)
            val max = list.subList(min + 1, list.size).indexOf(number - a)
            if (max >= 0) return Pair(min, max + min + 1)
        }
    }
    return Pair(-1, -1)
}

/**
 * Очень сложная (8 баллов)
 *
 * Входными данными является ассоциативный массив
 * "название сокровища"-"пара (вес сокровища, цена сокровища)"
 * и вместимость вашего рюкзака.
 * Необходимо вернуть множество сокровищ с максимальной суммарной стоимостью,
 * которые вы можете унести в рюкзаке.
 *
 * Перед решением этой задачи лучше прочитать статью Википедии "Динамическое программирование".
 *
 * Например:
 *   bagPacking(
 *     mapOf("Кубок" to (500 to 2000), "Слиток" to (1000 to 5000)),
 *     850
 *   ) -> setOf("Кубок")
 *   bagPacking(
 *     mapOf("Кубок" to (500 to 2000), "Слиток" to (1000 to 5000)),
 *     450
 *   ) -> emptySet()
 */
fun bagPacking(treasures: Map<String, Pair<Int, Int>>, capacity: Int): Set<String> {
    if (treasures.values.sumOf { it.first } <= capacity) return treasures.keys
    val curBags = mutableListOf<Triple<Int, Int, Set<String>>>()
    val prevBags = mutableListOf(Triple(0, 0, emptySet<String>()))
    treasures.forEach { name, (weight, cost) ->
        prevBags.forEach addItem@{ (load, total, set) ->
            if (load + weight <= capacity)
                curBags.add(Triple(load + weight, total + cost, set + name))
            else return@addItem // нет смысла продолжать
        }
        curBags.addAll(prevBags)
        curBags.sortBy { it.first }
        prevBags.clear()
        var maxTotal = -1
        prevBags.addAll(curBags.filter { (_, total, _) ->
            if (maxTotal >= total) {
                false
            } else {
                maxTotal = total
                true
            }
        })
        curBags.clear()
    }
    return prevBags.last().third
}


/**
 * Валентин и Настя женятся! Они отослали всем друзьям приглашения
 * на свадьбу. На каждом приглашении можно отметить +num,
 * где num - количество родственников, которые придут с
 * приглашенным человеком.
 *
 * К сожалению, Валентин и Настя не знают арабских и римских чисел,
 * и поэтому просят гостей писать количество родственников прописью.
 * При этом количество родственников не может быть больше 10.
 * Список отметок на приглашениях указан параметром marks,
 * например listOf("Даниил+два", "Катя+десять")
 *
 * Сколько же потратит молодая пара на праздничный обед, если
 * обслуживание одного человека указано параметром cost?
 *
 * Пример входных данных: listOf("Даниил+два", "Катя+десять")
 *
 * Имя функции и тип результата функции предложить
 * самостоятельно; в задании указан тип Any.
 *
 * При нарушении формата входной строки
 * бросить IllegalArgumentException
 *
 * Кроме функции, следует написать тесты,
 * подтверждающие её работоспособность.
 */
fun costParty(marks: List<String>, cost: Int): Int {
    var total = 0
    val num = mapOf(
        "ноль" to 0,
        "один" to 1,
        "два" to 2,
        "три" to 3,
        "четыре" to 4,
        "пять" to 5,
        "шесть" to 6,
        "семь" to 7,
        "восемь" to 8,
        "девять" to 9,
        "десять" to 10
    )
    marks.forEach {
        if ('+' !in it) {
            total += cost
        } else
            with(it.split("+")) {
                if (count() != 2) throw IllegalArgumentException()
                val numStr = this[1].trim().toLowerCase()
                if (numStr in num) {
                    total += cost + cost * num.getValue(numStr)
                } else {
                    throw IllegalArgumentException()
                }
            }
    }
    return total
}


